                <li><strong>Cybersecurity</strong>: Involves ethical hacking, network security, and cryptography.</li>
                <li><strong>Design/UI/UX</strong>: Focuses on creating user-friendly interfaces and user experiences.</li>
                <li><strong>Blockchain/Cryptocurrency</strong>: Focuses on decentralized applications, blockchain development, and cryptocurrency systems.</li>
                <li><strong>Internet of Things (IoT)</strong>: Focuses on integrating devices and sensors with the internet for smart applications.</li>
                <li><strong>Augmented Reality (AR) and Virtual Reality (VR)</strong>: Focuses on immersive technology to create interactive virtual environments.</li>
            </ul>
