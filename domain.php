            <h2>1. Software Development (Tech/Software Engineering)</h2>
            <p>Software development is one of the most common domains for hackathons. It involves building web or mobile applications, solving technical problems, and coding in various languages.</p>
            <h3>Key Knowledge Areas</h3>
            <ul>
                <li><strong>Languages:</strong> JavaScript, Python, Java, C++, Ruby, Go</li>
                <li><strong>Web Development:</strong> HTML, CSS, JavaScript, React, Angular, Node.js, Django</li>
                <li><strong>Databases:</strong> SQL (MySQL, PostgreSQL), NoSQL (MongoDB)</li>
                <li><strong>Cloud Services:</strong> AWS, Google Cloud, Azure</li>
            </ul>
        </section>

        <section>
            <h2>2. Data Science & Machine Learning (AI/ML)</h2>
            <p>In this domain, participants build models to solve data-driven problems, from predicting outcomes to identifying patterns in data.</p>
            <h3>Key Knowledge Areas</h3>
            <ul>
                <li><strong>Languages:</strong> Python, R</li>
                <li><strong>Data Analysis:</strong> Pandas, NumPy, Matplotlib, Seaborn</li>
                <li><strong>Machine Learning:</strong> Scikit-learn, TensorFlow, PyTorch</li>
                <li><strong>Deep Learning:</strong> Neural Networks, CNNs, RNNs</li>
                <li><strong>Natural Language Processing:</strong> BERT, GPT, spaCy</li>
            </ul>
        </section>

        <section>
            <h2>3. Cybersecurity</h2>
            <p>Cybersecurity focuses on defending systems and networks from attacks, ensuring data privacy, and securing applications from vulnerabilities.</p>
            <h3>Key Knowledge Areas</h3>
            <ul>
                <li><strong>Ethical Hacking:</strong> Penetration testing, Kali Linux, Metasploit</li>
                <li><strong>Cryptography:</strong> Encryption algorithms, public/private keys, SSL/TLS</li>
                <li><strong>Network Security:</strong> Wireshark, DDoS attacks, firewall configuration</li>
                <li><strong>Web Security:</strong> OWASP, XSS, SQL Injection</li>
            </ul>
        </section>

        <section>
            <h2>4. Design (UI/UX Design)</h2>
            <p>Designers create user interfaces and experiences that are intuitive, visually appealing, and easy to navigate. This domain is crucial for hackathons focused on apps, websites, and user-driven products.</p>
            <h3>Key Knowledge Areas</h3>
            <ul>
                <li><strong>UI Design:</strong> Color theory, typography, layout, design tools like Sketch, Figma, Adobe XD</li>
                <li><strong>UX Design:</strong> Wireframing, prototyping, user research, user personas</li>
                <li><strong>Design Systems:</strong> Consistency, reusable components, UI kits</li>
            </ul>
        </section>

        <section>
